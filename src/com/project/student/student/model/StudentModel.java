package com.project.student.student.model;

public class StudentModel {
	private String codeStudent;
	private String nameStudent;
	private String sexStudent;
	private String telStudent;
	private String academyStudent;
	private String professionalStudent;
	private String gradeStudent;
	private String classStudent;
	private String dormitoryStudent;
	
	public void setCodeStudent(String code) {
		this.codeStudent = code;
	}
	public String getCodeStudent() {
		return codeStudent;
	}
	
	public void setNameStudent(String name) {
		this.nameStudent = name;
	}
	public String getNameStudent() {
		return nameStudent;
	}
	
	public void setSexStudent(String sex) {
		this.sexStudent = sex;
	}
	public String getSexStudent() {
		return sexStudent;
	}
	
	public void setTelStudent(String tel) {
		this.telStudent = tel;
	}
	public String getTelStudent() {
		return telStudent;
	}
	
	public void setAcademyStudent(String academy) {
		this.academyStudent = academy;
	}
	public String getAcademyStudent() {
		return academyStudent;
	}
	
	public void setProfessionalStudent(String professional) {
		this.professionalStudent = professional;
	}
	public String getProfessionalStudent() {
		return professionalStudent;
	}

	public void setGradeStudent(String grade) {
		this.gradeStudent = grade;
	}
	public String getGradeStudent() {
		return gradeStudent;
	}
	
	public void setClassStudent(String classes) {
		this.classStudent = classes;
	}
	public String getClassStudent() {
		return classStudent;
	}
	
	public void setDormitoryStudent(String dormitory) {
		this.dormitoryStudent = dormitory;
	}
	public String getDormitoryStudent() {
		return dormitoryStudent;
	}



}
