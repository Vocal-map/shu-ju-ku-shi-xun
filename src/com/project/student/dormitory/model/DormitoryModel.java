package com.project.student.dormitory.model;

public class DormitoryModel {
	private String codeDormitory;
	private String capacityDormitory;
	private String priceDormitory;
	
	public void setCodeDormitory(String code) {
		this.codeDormitory = code;
	}
	public String getCodeDormitory() {
		return codeDormitory;
	}
	
	public void setCapacityDormitory(String capacity) {
		this.capacityDormitory = capacity;
	}
	public String getCapacityDormitory() {
		return capacityDormitory;
	}
	
	public void setPriceDormitory(String price) {
		this.priceDormitory = price;
	}
	public String getPriceDormitory() {
		return priceDormitory;
	}
}
